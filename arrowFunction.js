const doMath = (a, b) => {
    const sum = a + b;
    console.log(`The sum is : ${sum}`);

    const diff = a - b;
    console.log(`The Difference is : ${diff}`);

    const product = a * b;
    console.log(`The Product is : ${product}`);

    const div = a / b;
    console.log(`The Division is: ${div}`);

}

doMath(12, 3);